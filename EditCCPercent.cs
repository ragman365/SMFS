                endDate = dt.Rows[i]["eDate"].ObjToDateTime();
                fee = dt.Rows[i]["fee"].ObjToDouble();

                G1.update_db_table("creditcard_fees", "record", record, new string[] { "beginDate", beginDate.ToString("MM/dd/yyyy"), "endDate", endDate.ToString("MM/dd/yyyy"), "fee", fee.ToString() });
            }
            modified = false;
            btnSaveAll.Hide();
        }
