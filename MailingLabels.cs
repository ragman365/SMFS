        /***********************************************************************************************/
        private void MailingLabels_Load(object sender, EventArgs e)
        {
            if (!isInsurance)
                gridMain.Columns["payer"].Visible = false;
            string address2 = "";
            string zip2 = "";
            for ( int i=0; i<workDt.Rows.Count; i++)
            {
                address2 = workDt.Rows[i]["address2"].ObjToString();
                if (!String.IsNullOrWhiteSpace(address2))
                    workDt.Rows[i]["address1"] += " " + address2;
                zip2 = workDt.Rows[i]["zip2"].ObjToString();
                if ( !String.IsNullOrWhiteSpace ( zip2))
                {
                    if (zip2 != "0")
                        workDt.Rows[i]["zip1"] += "-" + zip2;
                }
            }
            G1.NumberDataTable(workDt);
            dgv.DataSource = workDt;
        }
